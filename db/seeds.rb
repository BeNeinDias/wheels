puts "Creating cars..."
3.times do
  BRANDS_MODELS.each_with_index do |brand_model, index|
    arr = brand_model.split(', ')
    brand = arr[0]
    model = arr[1]
    address_info = ADDRESSES.sample.split(' - ')
    address = address_info[0]
    city = address_info[1]
    country = address_info[2]
    photo = IMAGES[index]
    year = rand(2000..2018)
    passengers = [2,4].sample
    color = COLORS.sample
    horsepower = [200,400,450,500,300,230,320,600,680,900,230,150,100].sample
    car_type = CarType.order("RANDOM()").first
    price_per_day = [30,40,50,60,70,80,90,100,110,120,150,500].sample
    fuel_type = %w[diesel petrol electric hybrid gas].sample
    user = User.order("RANDOM()").first
    Car.create!(brand: brand, model: model, year: year, passengers: passengers,
      color: color, horsepower: horsepower, car_type: car_type, price_per_day: price_per_day,
      fuel_type: fuel_type, city: city, country: country, user_id: user.id, address: address, photo: photo)
  end
end
puts "#{Car.all.length} Cars created!"


puts "Creating bookings..."
cars = Car.all
cars.each do |car|
  rand(1..4).times do
    user = User.order("RANDOM()").first
    car = car
    date_from = [*DateTime.new(2018, 1, 1)..DateTime.now].sample
    date_to = date_from + rand(10)
    price = (date_to - date_from).to_i * car.price_per_day
    Booking.create!(user_id: user.id, car_id: car.id, date_from: date_from,
      date_to: date_to, price: price)
  end
end
puts "#{Booking.all.length} Bookings created!"


